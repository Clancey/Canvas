using System;

namespace Xamarin.Canvas
{
	public struct Point
	{
		public double X;
		public double Y;

		public Point (double x, double y)
		{
			X = x;
			Y = y;
		}
	}

	public struct Vec2
	{
		public double X;
		public double Y;

		public Vec2 (double x, double y)
		{
			X = x;
			Y = y;
		}
	}
}
